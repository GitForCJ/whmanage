package com.wlt.webm.business.Car;

public class CarQueryInfo {
	private String wzId;
	private String wzNum;
	private String wzAddress;
	private String wzMoney;
	private String wzState;
	private String orderTime;
	public String getWzId() {
		return wzId;
	}
	public void setWzId(String wzId) {
		this.wzId = wzId;
	}
	public String getWzNum() {
		return wzNum;
	}
	public void setWzNum(String wzNum) {
		this.wzNum = wzNum;
	}
	public String getWzAddress() {
		return wzAddress;
	}
	public void setWzAddress(String wzAddress) {
		this.wzAddress = wzAddress;
	}
	public String getWzMoney() {
		return wzMoney;
	}
	public void setWzMoney(String wzMoney) {
		this.wzMoney = wzMoney;
	}
	public String getWzState() {
		return wzState;
	}
	public void setWzState(String wzState) {
		this.wzState = wzState;
	}
	public String getOrderTime() {
		return orderTime;
	}
	public void setOrderTime(String orderTime) {
		this.orderTime = orderTime;
	}
}
