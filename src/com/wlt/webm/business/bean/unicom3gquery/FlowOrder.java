package com.wlt.webm.business.bean.unicom3gquery;

import java.util.List;

public class FlowOrder {
	private List<FlowOrderDetail> orders;
	private int status;
    private String desc;
    
	public List<FlowOrderDetail> getOrders() {
		return orders;
	}

	public void setOrders(List<FlowOrderDetail> orders) {
		this.orders = orders;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}
	
}
