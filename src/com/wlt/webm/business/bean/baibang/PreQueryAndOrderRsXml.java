package com.wlt.webm.business.bean.baibang;

import javax.xml.bind.annotation.XmlRootElement;

/**
 * 
 * @author 1989
 *
 */
@XmlRootElement(name = "PreQueryAndOrderRsXml")
public class PreQueryAndOrderRsXml extends XmlHead{

	private String CftTransId;
	private String SpTransId;
	private String ViolationId;
	private String SpRetCode;
	private String SpRetInfo;
	
	public PreQueryAndOrderRsXml(String Attach, String Sign) {
		super(Attach, Sign);
	}

	public String getCftTransId() {
		return CftTransId;
	}

	public void setCftTransId(String cftTransId) {
		CftTransId = cftTransId;
	}

	public String getSpTransId() {
		return SpTransId;
	}

	public void setSpTransId(String spTransId) {
		SpTransId = spTransId;
	}

	public String getViolationId() {
		return ViolationId;
	}

	public void setViolationId(String violationId) {
		ViolationId = violationId;
	}

	public String getSpRetCode() {
		return SpRetCode;
	}

	public void setSpRetCode(String spRetCode) {
		SpRetCode = spRetCode;
	}

	public String getSpRetInfo() {
		return SpRetInfo;
	}

	public void setSpRetInfo(String spRetInfo) {
		SpRetInfo = spRetInfo;
	}
}
