package com.wlt.webm.business.dianx.form;

import java.util.List;

import org.apache.struts.action.ActionForm;



public class PhonePayForm extends ActionForm {

	/**
	 * 固话号码
	 */
	private String tradeObject;
	/**
	 * 消息
	 */
	private String message;
	/**
	 * 用户名称
	 */
	private String userName;
	/**
	 * 累计充值金额
	 */
	private String totalFee;
	/**
	 * 账单信息列表
	 */
	private List billList;
	/**
	 * 交易流水号
	 */
	private String seqNo;
	/**
	 * 交易金额
	 */
	private String payFee;
	/**
	 * 交易返回码
	 */
	private String state;
	
	/**
	 * 号码类型
	 */
	private String numType;
	
	/**
	 * @return 交易金额
	 */ 
	public String getPayFee() {
		return payFee;
	}

	/**
	 * @param payFee 交易金额
	 */
	public void setPayFee(String payFee) {
		this.payFee = payFee;
	}

	/**
	 * @return 固话号码
	 */
	public String getTradeObject() {
		return tradeObject;
	}
	
	/**
	 * @param tradeObject 固话号码
	 */
	public void setTradeObject(String tradeObject) {
		this.tradeObject = tradeObject;
	}

	/**
	 * @return 消息
	 */
	public String getMessage() {
		return message;
	}

	/**
	 * @param message 消息
	 */
	public void setMessage(String message) {
		this.message = message;
	}

	/**
	 * @return 账单信息列表
	 */
	public List getBillList() {
		return billList;
	}

	/**
	 * @param billList 账单信息列表
	 */
	public void setBillList(List billList) {
		this.billList = billList;
	}

	/**
	 * @return 交易流水号
	 */
	public String getSeqNo() {
		return seqNo;
	}

	/**
	 * @param seqNo 交易流水号
	 */
	public void setSeqNo(String seqNo) {
		this.seqNo = seqNo;
	}

	/**
	 * @return 累计充值金额
	 */
	public String getTotalFee() {
		return totalFee;
	}

	/**
	 * @param totalFee 累计充值金额
	 */
	public void setTotalFee(String totalFee) {
		this.totalFee = totalFee;
	}

	/**
	 * @return 用户名称
	 */
	public String getUserName() {
		return userName;
	}

	/**
	 * @param userName 用户名称
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}

	/**
	 * @return state 交易返回码
	 */
	public String getState() {
		return state;
	}

	/**
	 * @param state 交易返回码
	 */
	public void setState(String state) {
		this.state = state;
	}

	public String getNumType() {
		return numType;
	}

	public void setNumType(String numType) {
		this.numType = numType;
	}
}
