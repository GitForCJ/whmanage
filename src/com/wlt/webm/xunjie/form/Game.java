package com.wlt.webm.xunjie.form;

import java.util.List;

public class Game {
	
	private String game_name;
	private String game_id;
	private List<Game_account_type> gameAccountTypeList;
	private List<Game_area> game_AreaList;
	public String getGame_name() {
		return game_name;
	}
	public void setGame_name(String game_name) {
		this.game_name = game_name;
	}
	public String getGame_id() {
		return game_id;
	}
	public void setGame_id(String game_id) {
		this.game_id = game_id;
	}
	public List<Game_account_type> getGameAccountTypeList() {
		return gameAccountTypeList;
	}
	public void setGameAccountTypeList(List<Game_account_type> gameAccountTypeList) {
		this.gameAccountTypeList = gameAccountTypeList;
	}
	public List<Game_area> getGame_AreaList() {
		return game_AreaList;
	}
	public void setGame_AreaList(List<Game_area> game_AreaList) {
		this.game_AreaList = game_AreaList;
	}
	
	

}
